#include "libft.h"

int	ft_strequ(char const *s1, char const *s2)
{
	int i;

	i = 0;
	if (s1 == 0 && s2 == 0)
		return (1);
	if ((s1 == 0 && s2 != 0) || (s1 != 0 && s2 == 0))
		return (0);
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	i = s1[i] - s2[i];
	if (i != 0)
		return (0);
	return (1);
}
