#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*q;
	int		i;

	q = (char *)s;
	i = 0;
	if (q[i] == '\0' && c != '\0')
		return (0);
	while (q[i] != c && q[i] != '\0')
	{
		if (q[i] == c)
			break ;
		i++;
	}
	if (q[i] == '\0' && c != '\0')
		return (0);
	return (&q[i]);
}
