#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*p;

	p = (unsigned char *)s;
	i = 0;
	while (i < n && p[i] != (unsigned char)c)
	{
		i++;
	}
	if (i == n)
		return (0);
	return (p + i);
}
