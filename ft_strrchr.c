#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*q;
	int		i;

	q = (char *)s;
	i = 0;
	if (q[i] == '\0' && c != '\0')
		return (0);
	while (q[i] != '\0')
		i++;
	while (q[i] != c && i > -1)
	{
		if (q[i] == c)
			break ;
		i--;
	}
	if (i == -1)
		return (0);
	return (&q[i]);
}
