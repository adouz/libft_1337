#include "libft.h"

void	ft_putnbr(int n)
{
	int z;

	z = 0;
	if (n == -2147483648)
	{
		z = 1;
		n += 1;
	}
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + z + '0');
	}
	if (n <= 9)
		ft_putchar(n + '0');
}
