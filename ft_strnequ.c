#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t i;

	if (n == 0)
		return (1);
	if (s1 == 0 && s2 == 0)
		return (1);
	if ((s1 == 0 && s2 != 0) || (s1 != 0 && s2 == 0))
		return (0);
	i = 0;
	while (i < n - 1 && s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	i = s1[i] - s2[i];
	if (i != 0)
		return (0);
	return (1);
}
